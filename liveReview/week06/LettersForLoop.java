package week06;

public class LettersForLoop {

    public static void main(String[] args) {

        for (int i = 65; i <= 90 ; i++) {
            System.out.print((char)i+" ");
        }

        System.out.println("\n______________________________________\n");

        for (char c = 'A' ; c <= 'Z' ; c++) {
            System.out.print(c+" ");
        }

        System.out.println("\n______________________________________\n");


    }
}
